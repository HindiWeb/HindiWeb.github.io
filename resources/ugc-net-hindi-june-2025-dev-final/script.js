function isArray(arr) {
  return Array.isArray(arr);
}
/**
 *   all measns args
 *   all should be arrays
 *   all should be the same length
 *   all should have questionId and answer
 *   all questionIds should be in all args
 *   throw error if not valid
 *   return true if valid
 * @param  {...any} args
 */
function isValid(...args) {
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (!arg) {
      throw new Error(`All args should be defined`);
    }
    if (!isArray(arg)) {
      throw new Error(`all args should be arrays`);
      return false;
    }
    if (arg.length !== args[0].length) {
      throw new Error(`all args should be the same length`);
      return false;
    }
    for (let j = 0; j < arg.length; j++) {
      const item = arg[j];
      if (
        !item.hasOwnProperty("questionId") ||
        !item.hasOwnProperty("answer")
      ) {
        throw new Error(`all args should have questionId and answer`);
        return false;
      }
    }
  }
  return true;
}

function cleanArrayData(arr) {
  return arr.map(obj => {
    const cleaned = {};
    for (const key in obj) {
      const value = obj[key];
      cleaned[key] = coerceType(value);
    }
    return cleaned;
  });
}

function coerceType(value) {
  if (typeof value !== 'string') return value;

  // Try boolean
  if (value.toLowerCase() === 'true') return true;
  if (value.toLowerCase() === 'false') return false;

  // Try number
  const num = Number(value);
  if (!isNaN(num) && value.trim() !== '') return num;

  // Fallback to original string
  return value;
}

